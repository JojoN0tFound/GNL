#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*t1;
	unsigned char	*t2;

	t1 = (unsigned char *)s1;
	t2 = (unsigned char *)s2;
	if (n != 0)
		while (n--)
			if (*t1++ != *t2++)
				return (*(t1 - 1) - *(t2 - 1));
	return (0);
}
